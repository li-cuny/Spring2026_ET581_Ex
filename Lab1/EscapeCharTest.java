public class EscapeCharTest{
    public static void main(String[] args){
        System.out.println("apple orange mango");
        System.out.println("apple\torange\tmango");
        System.out.print("apple\norange\nmango\n");

        System.out.printf("This is digit print: %d on console.\n", 12);
        System.out.printf("This is digit print: %f on console.\n", 12.0);
        System.out.printf("This is digit print: %s on console.\n", "String");
        // %f floating number
        // %s string print
    }
}